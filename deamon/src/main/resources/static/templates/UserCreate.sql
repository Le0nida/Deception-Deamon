create table User (
	id INT,
	first_name VARCHAR(50),
	last_name VARCHAR(50),
	email VARCHAR(50),
	username VARCHAR(50),
	password VARCHAR(50),
	gender VARCHAR(50),
	ipaddress VARCHAR(20),
	address_id INT,
	phone_number VARCHAR(50),
	job_title VARCHAR(50),
	department VARCHAR(50),
	company_id INT,
	isActive VARCHAR(50)
);